    def is_greeting(self, query: str) -> bool:
        """تشخیص سلام و احوالپرسی"""
        query_clean = query.strip()
        
        # بررسی الگوهای سلام
        for pattern in self.greeting_patterns:
            if re.match(pattern, query_clean, re.IGNORECASE):
                return True
        
        # بررسی کلمات کلیدی سلام
        greeting_words = ['سلام', 'درود', 'صبح بخیر', 'ظهر بخیر', 'عصر بخیر', 'شب بخیر']
        query_lower = query_clean.lower()
        for word in greeting_words:
            if word in query_lower and len(query_clean.split()) <= 5:
                return True
        
        return False
    
    def is_irrelevant(self, query: str) -> bool:
        """تشخیص سوالات نامرتبط با برنامه و بودجه"""
        query_clean = query.strip().lower()
        query_clean = query_clean.replace('\u200c', ' ')  # حذف نیم‌فاصله
        query_clean = re.sub(r'\s+', ' ', query_clean)
        
        # اگر هیچ کلمه کلیدی مرتبط با بودجه نداشته باشد
        has_budget_keyword = any(keyword.lower() in query_clean for keyword in self.budget_keywords_core)
        has_domain_anchor = any(keyword.lower() in query_clean for keyword in self.domain_anchor_keywords)
        domain_signal = has_budget_keyword or has_domain_anchor
        
        # اگر کلمات نامرتبط داشته باشد و کلمات مرتبط نداشته باشد
        has_irrelevant_keyword = any(keyword in query_clean for keyword in self.irrelevant_keywords)
        
        # اگر کلمات نامرتبط دارد و کلمات مرتبط ندارد، نامرتبط است
        if has_irrelevant_keyword and not domain_signal:
            return True
        
        # بررسی الگوهای خاص نامرتبط
        irrelevant_patterns = [
            r'هوا.*چطور',
            r'چه.*فیلم',
            r'کدام.*فیلم',
            r'امروز.*چی',
            r'چه.*ببینم',
            r'کتاب.*خوب',
            r'موسیقی.*چی',
            r'ورزش.*چی',
            r'آب.?و.?هوا',
        ]
        
        for pattern in irrelevant_patterns:
            if re.search(pattern, query_clean):
                if not domain_signal:
                    return True
        
        # اگر طول سوال کوتاه است و هیچ کلمه مرتبطی ندارد
        if len(query_clean.split()) <= 4 and not domain_signal:
            # بررسی اینکه آیا سوال است یا نه
            question_words = ['چیست', 'چی', 'چطور', 'چرا', 'کجا', 'کدام', 'چه', 'چقدر', 'چند']
            is_question = any(word in query_clean for word in question_words)
            
            if is_question and not domain_signal:
                return True
        
        return False
    
    def normalize_financial_terms(self, query: str) -> str:
        """
        تبدیل اصطلاحات مالی:
        - "منابع" -> "درآمد"
        - "مصارف" -> "هزینه"
        """
        normalized = query
        
        # تبدیل "منابع" به "درآمد"
        # فقط اگر قبلاً "درآمد" در query نباشد (تا "منبع درآمد" تبدیل نشود به "درآمد درآمد")
        if 'منابع' in normalized and 'درآمد' not in normalized:
            normalized = normalized.replace('منابع', 'درآمد')
            logger.info("🔄 Converted 'منابع' to 'درآمد'")
        elif 'منبع' in normalized and 'منابع' not in normalized and 'درآمد' not in normalized:
            # فقط اگر "منابع" و "درآمد" نباشد
            normalized = normalized.replace('منبع', 'درآمد')
            logger.info("🔄 Converted 'منبع' to 'درآمد'")
        
        # تبدیل "مصارف" به "هزینه"
        # فقط اگر قبلاً "هزینه" در query نباشد (تا "مصرف هزینه" تبدیل نشود به "هزینه هزینه")
        if 'مصارف' in normalized and 'هزینه' not in normalized:
            normalized = normalized.replace('مصارف', 'هزینه')
            logger.info("🔄 Converted 'مصارف' to 'هزینه'")
        elif 'مصرف' in normalized and 'مصارف' not in normalized and 'هزینه' not in normalized:
            # فقط اگر "مصارف" و "هزینه" نباشد
            # اما "مصرف" را فقط در context مالی تبدیل می‌کنیم
            financial_context = any(word in normalized for word in [
                'درآمد', 'بودجه', 'مالی', 'مالیاتی',
                'عمومی', 'اختصاصی', 'جمع', 'مجموع',
                'دستگاه', 'سازمان', 'بخش', 'بند'
            ])
            
            if financial_context:
                normalized = normalized.replace('مصرف', 'هزینه')
                logger.info("🔄 Converted 'مصرف' to 'هزینه'")
        
        return normalized
    
    def preprocess(self, query: str) -> Dict[str, Any]:
        """
        پیش‌پردازش کامل سوال
        
        Returns:
            Dict با کلیدهای:
            - processed_query: سوال پردازش شده
            - is_greeting: آیا سلام است؟
            - is_irrelevant: آیا نامرتبط است؟
            - greeting_response: پاسخ برای سلام (اگر سلام باشد)
            - irrelevant_response: پاسخ برای سوال نامرتبط (اگر نامرتبط باشد)
            - should_process: آیا باید پردازش شود؟
        """
        result = {
            'processed_query': query,
            'is_greeting': False,
            'is_irrelevant': False,
            'greeting_response': None,
            'irrelevant_response': None,
            'should_process': True
        }
        
        # بررسی سلام
        if self.is_greeting(query):
            result['is_greeting'] = True
            result['should_process'] = False
            result['greeting_response'] = self._generate_greeting_response()
            logger.info("👋 Greeting detected")
            return result
        
        # نرمال‌سازی اصطلاحات مالی
        normalized_query = self.normalize_financial_terms(query)
        result['processed_query'] = normalized_query
        
        # بررسی نامرتبط بودن
        if self.is_irrelevant(normalized_query):
            result['is_irrelevant'] = True
            result['should_process'] = False
            result['irrelevant_response'] = self._generate_irrelevant_response()
            logger.info("🚫 Irrelevant query detected")
            return result
        
        return result
    
