    def _should_skip_currency_conversion(self, collection_name: Optional[str]) -> bool:
        """
        تشخیص اینکه آیا collection باید از تبدیل اعداد به ریال معاف باشد.
        
        Collection های غیرمالی (booklet_bo, zinaf_dakheli, karbaran_omomi, etc.)
        نباید اعداد را به ریال تبدیل کنند چون اعداد در آن‌ها کد یا شماره هستند، نه مبالغ مالی.
        """
        if not collection_name:
            return False
        collection_lower = collection_name.lower()
        # Collection های غیرمالی که نباید تبدیل اعداد انجام شود
        non_financial_keywords = [
            "booklet_bo", "booklet__bo",
            "zinaf_dakheli", "zinaf", "dakheli",
            "karbaran_omomi", "karbaran", "omomi"
        ]
        return any(keyword in collection_lower for keyword in non_financial_keywords)

    def _is_dynamic_collection(self, collection_name: Optional[str]) -> bool:
        """آیا این collection از طریق API ساخته شده (col_XXXX) است؟"""
        return bool(collection_name and str(collection_name).startswith("col_"))

    def _classify_general_query(self, query: str) -> str:
        """
        برای کالکشن‌های دینامیک: نوع سوال را تشخیص بده.
        Returns: 'identity' | 'content'
        """
        q = query.strip()
        identity_patterns = [
            'تو درباره', 'درباره چی هستی', 'درباره چیستی', 'درباره چی', 'درباره چه',
            'تو کی هستی', 'کی هستی', 'چی هستی', 'چیستی',
            'تو چی هستی', 'تو چیستی', 'هویتت', 'هویت تو',
            'سلام', 'درود', 'صبح بخیر', 'ظهر بخیر', 'عصر بخیر', 'شب بخیر',
            'چطوری', 'چطور هستی', 'حالت', 'خوبی',
            'معرفی خودت', 'معرفی کن', 'چه کاری', 'چه کار',
            'چه می‌دانی', 'چی می‌دونی', 'چی بلدی',
        ]
        q_lower = q.lower()
        for p in identity_patterns:
            if p in q_lower:
                return 'identity'
        # سوال‌های بسیار کوتاه (۱-۲ کلمه) که سوال محتوایی نیستند
        if len(q.split()) <= 2 and '?' not in q and '؟' not in q:
            return 'identity'
        return 'content'

